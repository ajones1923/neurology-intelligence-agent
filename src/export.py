"""Report export system for the Neurology Intelligence Agent.

Supports four export formats:
  - export_markdown()      -- structured clinical Markdown report
  - export_json()          -- machine-readable JSON
  - export_pdf()           -- styled PDF via ReportLab Platypus
  - export_fhir_r4()       -- FHIR R4 DiagnosticReport Bundle

Plus specialised report generators:
  - export_stroke_report()          -- acute stroke triage report
  - export_dementia_evaluation()    -- dementia workup report
  - export_epilepsy_report()        -- epilepsy monitoring report
  - export_scale_report()           -- clinical scale calculation report

Author: Adam Jones
Date: March 2026
"""

